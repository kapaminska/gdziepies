/**
 * Lista wszystkich województw w Polsce (alfabetycznie).
 */
export const voivodeships = Object.keys(polishLocations).sort();

/**
 * Pobiera listę powiatów dla danego województwa.
 * @param voivodeship - Nazwa województwa
 * @returns Lista powiatów lub pustą tablicę, jeśli województwo nie istnieje
 */
export function getPoviatsForVoivodeship(voivodeship: string): string[] {
  return polishLocations[voivodeship] || [];
}

/**
 * Sprawdza, czy powiat należy do danego województwa.
 * @param voivodeship - Nazwa województwa
 * @param poviat - Nazwa powiatu
 * @returns true, jeśli powiat należy do województwa
 */
export function isValidPoviatForVoivodeship(voivodeship: string, poviat: string): boolean {
  const poviats = getPoviatsForVoivodeship(voivodeship);
  return poviats.includes(poviat);
}


